def reverse_engineer(model_file, ref_model=None, input_shape=(3, 32, 32)):
    """Reconstruct architecture from state_dict and compute fidelity % (Section IV-D).

    Fidelity = fraction of reconstructed weight tensors whose shape exactly matches
    the reference model's state_dict (a proxy for structural reconstruction success).
    Baseline ~92.4%, GHOST ~14.6% (Section IV-D).
    """
    try:
        state_dict = torch.load(model_file, map_location='cpu')
    except Exception as e:
        print("Error loading .pth file:", e)
        return None, 0.0

    if isinstance(state_dict, dict) and 'state_dict' in state_dict:
        state_dict = state_dict['state_dict']

    print(f"\nKeys in state_dict: {list(state_dict.keys())[:6]} ...\n")

    layers = []
    layer_num = 0
    for key, weight in state_dict.items():
        if '.weight' not in key:
            continue
        if 'conv' in key:
            out_ch, in_ch, kH, kW = weight.shape
            layers.append((f'conv{layer_num}', nn.Conv2d(in_ch, out_ch, (kH, kW))))
        elif 'fc' in key or 'linear' in key:
            out_f, in_f = weight.shape
            layers.append((f'fc{layer_num}', nn.Linear(in_f, out_f)))
        layer_num += 1

    recon_model = nn.Sequential(OrderedDict(layers))
    print("Reconstructed Model Architecture:\n", recon_model)

    # Fidelity: shape-match fraction against reference (Section IV-D)
    fidelity = 0.0
    if ref_model is not None:
        ref_sd = ref_model.state_dict() if hasattr(ref_model, 'state_dict') else ref_model
        recon_sd = recon_model.state_dict()
        ref_shapes  = [tuple(v.shape) for v in ref_sd.values()]
        recon_shapes = [tuple(v.shape) for v in recon_sd.values()]
        matched = sum(rs == rr for rs, rr in zip(ref_shapes, recon_shapes))
        total   = max(len(ref_shapes), 1)
        fidelity = matched / total * 100
        print(f"Reconstruction fidelity: {fidelity:.1f}%  ({matched}/{total} layers matched)")
    else:
        print("Parameter shapes:")
        for k, v in state_dict.items():
            print(f"  {k:<40} {tuple(v.shape)}")

    return recon_model, fidelity

