def substitute_model_attack(target_model, datapoints=5000, mtype=""):
    target_model.eval()
    target_model.to(device)

    # Use a standard transform
    transform = transforms.Compose([
        transforms.Resize((32,32)),
        transforms.RandomHorizontalFlip(),
        transforms.RandomCrop(32, padding=4),
        transforms.ToTensor(),
        transforms.Normalize((0.5,)*3, (0.5,)*3)
    ])

    full_dataset = datasets.CIFAR10(root='./data', train=True, download=True, transform=transform)
    test_dataset = datasets.CIFAR10(root='./data', train=False, download=True, transform=transform)

    # Use a CIFAR10 subset to query the target model
    indices = np.random.choice(len(full_dataset), datapoints, replace=False)
    query_data = Subset(full_dataset, indices)
    query_loader = DataLoader(query_data, batch_size=64, shuffle=False)

    # Query the target model to get "soft labels"
    queried_images = []
    queried_labels = []

    with torch.no_grad():
        for inputs, _ in query_loader:
            inputs = inputs.to(device)
            if mtype == 'hybrid':
                outputs = predict(target_model, inputs, token=_token_hex)
            else:
                outputs = target_model(inputs)
            preds = outputs.argmax(dim=1).cpu()
            queried_images.append(inputs.cpu())
            queried_labels.append(preds)

    queried_images = torch.cat(queried_images)
    queried_labels = torch.cat(queried_labels)

    # Our simple substitute model
    class SimpleCNN(nn.Module):
        def __init__(self):
            super(SimpleCNN, self).__init__()
            self.conv1 = nn.Conv2d(3, 32, 3, padding=1)
            self.conv2 = nn.Conv2d(32, 64, 3, padding=1)
            self.pool = nn.MaxPool2d(2, 2)
            self.fc1 = nn.Linear(64 * 8 * 8, 128)
            self.fc2 = nn.Linear(128, 10)

        def forward(self, x):
            x = self.pool(F.relu(self.conv1(x)))
            x = self.pool(F.relu(self.conv2(x)))
            x = x.view(-1, 64 * 8 * 8)
            x = F.relu(self.fc1(x))
            return self.fc2(x)

    substitute_model = SimpleCNN().to(device)

    # Training our substitute model our of the labels we got from the target model
    optimizer = optim.Adam(substitute_model.parameters(), lr=0.001)
    criterion = nn.CrossEntropyLoss()

    train_loader = DataLoader(
        list(zip(queried_images, queried_labels)),
        batch_size=64, shuffle=True
    )

    for epoch in range(10):
        substitute_model.train()
        total_loss = 0
        for x_batch, y_batch in train_loader:
            x_batch, y_batch = x_batch.to(device), y_batch.to(device)
            optimizer.zero_grad()
            outputs = substitute_model(x_batch)
            loss = criterion(outputs, y_batch)
            loss.backward()
            optimizer.step()
            total_loss += loss.item()
        print(f"[Epoch {epoch+1}] Loss: {total_loss / len(train_loader):.4f}")

    # Evaluate substitute model on standard test set
    substitute_model.eval()
    test_loader = DataLoader(test_dataset, batch_size=64, shuffle=False)

    all_preds = []
    all_labels = []

    with torch.no_grad():
        for x_batch, y_batch in test_loader:
            x_batch = x_batch.to(device)
            outputs = substitute_model(x_batch)
            preds = outputs.argmax(dim=1).cpu()
            all_preds.extend(preds.numpy())
            all_labels.extend(y_batch.numpy())

    acc = accuracy_score(all_labels, all_preds)
    print("\n=== Attack Summary ===")
    print(f"Substitute Model Accuracy on CIFAR-10 Test Set: {acc * 100:.2f}%")

    return acc * 100

