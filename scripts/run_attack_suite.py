def run_local(model, x, path_key=None):
    x = model.unshuffle(x)
    out = F.relu(model.bn1(model.conv1(x)))
    out = model.pool(F.relu(model.bn2(model.conv2(out))))
    if path_key is not None:
        idx = path_key % len(model.fakes)
        out = model.fakes[idx](out)
    return out

def run_cloud(model, out):
    # Simulate latency based on output size (number of elements)
    simulated_latency = out.numel() * 1e-8  # 10 nanoseconds per element (example)
    time.sleep(simulated_latency)

    out = F.relu(model.bn3(model.conv3(out)))   # conv3 stays at 8×8; no pool (Section F)
    out = out.view(out.size(0), -1)
    out = model.dropout(F.relu(model.fc1(out)))
    return out

def predict(model, x, token=''):
    key = torch.randint(0, len(model.fakes), (1,)).item()  # random per inference (Section IV-A)
    return model(x, path_key=key, token=token)



